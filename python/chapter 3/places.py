# Assign list to the variable places
places = ['london', 'new york', 'tokyo', 'berlin', 'madrid']
print(places)
# printing list i alphabetical order using sorted function
print(sorted(places))

# print original list
print(places)

# printing reverse alphabet using sorted reverse
print(sorted(places, reverse=True))

# print original alphabet list
print(places)

# print in reverse using reverse() function
places.reverse()
print(places)

# print reverse using reverse function to change it back to it oringal form
places.reverse()
print(places)

# using sort to print alphabet permantly
places.sort()
print(places)

# using reverse alphabet
places.sort(reverse=True)
print(places)
